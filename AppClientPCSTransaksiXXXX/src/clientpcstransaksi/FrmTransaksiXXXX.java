    private void txtUangDibayarkanKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtUangDibayarkanKeyReleased
        // TODO add your handling code here:
        if (txtUangDibayarkan.getText().isEmpty()) {
            txtUangDibayarkan.setText("0");
        }
        double total = 0;
        double dibayar = 0;
        try {
            total = new DecimalFormat("#,##0.00").parse(
                    lblTotal.getText()).doubleValue();
            dibayar = new DecimalFormat("#,##0").parse(
                    txtUangDibayarkan.getText()).doubleValue();
        } catch (ParseException ex) {
            Logger.getLogger(FrmTransaksiXXXX.class.getName()).log(
                    Level.SEVERE, null, ex);
        }

        txtUangDibayarkan.setText(dfo.format(dibayar));
        double uangKembali = Double.valueOf(dibayar - total);
        lblUangKembali.setText(dfo.format(uangKembali));
    }//GEN-LAST:event_txtUangDibayarkanKeyReleased

