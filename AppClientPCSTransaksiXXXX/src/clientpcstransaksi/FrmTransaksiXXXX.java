    public void aturTblItem() {
        dtmItem = (DefaultTableModel) tblItem.getModel();
        dtmItem.setRowCount(0);
        for (int i = 0; i < listPro.size(); i++) {
            dtmItem.addRow(
                    new Object[]{listPro.get(i).getKode(), listPro.get(i).getNama(),
                        listPro.get(i).getHarga(), listPro.get(i).getDiskon()});
        }
    }

    public void hitungTotal() {
        double total = 0;
        if (dtmItem.getColumnCount() > 0) {
            for (int i = 0; i < dtmItem.getRowCount(); i++) {
                total += Double.valueOf(dtmItem.getValueAt(i, 5).toString());
            }
        }
        lblTotal.setText(String.valueOf(dfo.format(total)));
    }

