    private void txtBanyakActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtBanyakActionPerformed
        // TODO add your handling code here:
        String kode = DtProduk.getKode();
        String nama = DtProduk.getNama();
        double harga = DtProduk.getHarga();
        double dis = DtProduk.getDiskon();
        int byk = Integer.valueOf(txtBanyak.getText());
        double subtotal = byk * harga - (harga * dis);
        dtmItem.addRow(new Object[]{kode, nama, byk, harga, dis, subtotal});
        hitungTotal();
        txtKdProduk.setText("");
        txtNama.setText("");
        txtBanyak.setText("1");
        txtKdProduk.requestFocus();
    }//GEN-LAST:event_txtBanyakActionPerformed

