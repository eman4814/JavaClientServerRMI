    private void txtKdProdukActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtKdProdukActionPerformed
        // TODO add your handling code here:
        if (!txtKdProduk.getText().isEmpty()) {
            for (int i = 0; i < listPro.size(); i++) {
                if (listPro.get(i).getKode().equals(txtKdProduk.getText())) {
                    DtProduk.setKode(listPro.get(i).getKode());
                    DtProduk.setNama(listPro.get(i).getNama());
                    DtProduk.setHarga(listPro.get(i).getHarga());
                    DtProduk.setDiskon(listPro.get(i).getDiskon());
                    break;
                }
            }
            txtNama.setText(DtProduk.getNama());
            txtBanyak.requestFocus();
        }
    }//GEN-LAST:event_txtKdProdukActionPerformed

